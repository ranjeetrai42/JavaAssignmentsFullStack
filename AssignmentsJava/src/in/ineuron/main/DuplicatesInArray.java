package in.ineuron.main;
import java.util.*;

public class DuplicatesInArray {

	public static void main(String[] args) {
		int n;
		@SuppressWarnings("resource")
		Scanner scan=new Scanner(System.in);
		n=scan.nextInt();
		int[] arr= new int[n];
		for(int i=0;i<n;i++) {
			arr[i]=scan.nextInt();
		}
		System.out.println();
	}

}
